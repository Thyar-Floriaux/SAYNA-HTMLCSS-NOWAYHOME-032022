var btnn= document.querySelector('.button1')
function hover(){
    btnn.style.backgroundColor="#B11313"
    btnn.style.color="#ffffff"
    btnn.style.boxShadow="0 15px 50px  #ffffff inset, 0 5px 50px  #ffffff,0 5px 50px  #ffffff"
        
}
btnn.addEventListener("mouseover",hover)
btnn.addEventListener("mouseout",unblurt)
function unblurt()
{
    btnn.style.backgroundColor="var(--white-ad-color)"
    btnn.style.color="var(--primary-ad-color)"
    btnn.style.boxShadow="none"

}







